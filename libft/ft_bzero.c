#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*res;
	size_t		i;

	if (s != NULL)
	{
		i = 0;
		res = s;
		while (n)
		{
			res[i] = 0;
			i++;
			n--;
		}
	}
}
